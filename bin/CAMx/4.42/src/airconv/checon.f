      SUBROUTINE CHECON (IOUT,IDONE)
C
C
C      PROGRAM READS THE CHEMPARM FILE THAT IS INPUT INTO
C      THE SAI AIRSHED MODEL FOR LA AND REFORMATS THIS FILE SO
C      IT CAN BE WRITTEN TO TAPE AND SENT TO SAI.
C
      INCLUDE 'nampar.cmd'
Ctcm  DIMENSION  MECHID(60)
      character*4  MECHID(60)
Ctcm  DIMENSION TNAME(10,20),SPNAME(10,100),
Ctcm &   UBSS(100),RBINT(100),UBINT(100),STOINM(10,100)
      character*4 TNAME(10,20),SPNAME(10,100),STOINM(10,100)
      DIMENSION UBSS(100),RBINT(100),UBINT(100)
      LOGICAL TEMP(100),PHOTO(100)
      LOGICAL REA(100),SSI(100),SSB(100)
Ctcm  DIMENSION COEF(100),ISPECN(10,100),RES(100),RBSS(100),
Ctcm $  RATE(100), ACTIV(100), REFTP(100)
      character*4 ISPECN(10,100)
      DIMENSION COEF(100),RES(100),RBSS(100),
     $  RATE(100), ACTIV(100), REFTP(100)
C
C   READ AND WRITE OUT FILE HEADER INFORMATION
C
      IDONE=1
      READ(7,2100)IFILE,NOTE, NSEG, NSPECS, IDATE, BEGTIM, JDATE, ENDTIM
      WRITE(9)    IFILE,NOTE,NSEG,NSPECS,IDATE,BEGTIM,JDATE,ENDTIM
      WRITE (IOUT,1005) IDATE, BEGTIM, JDATE, ENDTIM
      IBHR=BEGTIM +1
      IEHR=ENDTIM
      IF (NSPECS.LE. 0) GO TO 10
      READ(7,1003)((MSPEC(M,L),M=1,10),L=1,NSPECS)
      WRITE (9) ((MSPEC(I,J),I=1,10),J=1,NSPECS)
  10  CONTINUE
C*
C
C  TIME INVARIANT DATA
C
      IF (NSPECS.LE.0) GO TO 999
C
C  TIME INVARIANT DATA - CHEM DEFN, SPEC PARA, REAC PARM, STOICH
C
C  CHEMICAL DEFINATION CARD
C
      READ (7,1004) (MECHID(I),I=1,60),NREAC,NCOEF
      WRITE(9) (MECHID(I),I=1,60),NREAC,NCOEF
C
C  SPECIES PARAMETER CARD
C
      READ (7,1005) ((ISPECN(II,I),II=1,10),REA(I),
     &   SSI(I),SSB(I),RES(I),RBSS(I),UBSS(I),RBINT(I),UBINT(I),
     &   I=1,NSPECS)

      WRITE(9)      ((ISPECN(II,I),II=1,10),REA(I),
     &   SSI(I),SSB(I),RES(I),RBSS(I),UBSS(I),RBINT(I),UBINT(I),
     &   I=1,NSPECS)
C
C  REACTION PARAMETERS
C
      READ(7,1006) (RATE(I),PHOTO(I),TEMP(I),ACTIV(I),REFTP(I),
     &   I=1,NREAC)

      WRITE(9)      (RATE(I),PHOTO(I),TEMP(I),ACTIV(I),REFTP(I),
     &   I=1,NREAC)
C
C  STOICHIOMETRIC COEFFICIENTS
C
      READ (7,1007) ((STOINM(II,J),II=1,10),COEF(J),J=1,NCOEF)
      WRITE(9)      ((STOINM(II,J),II=1,10),COEF(J),J=1,NCOEF)
  999 RETURN
 1002 FORMAT(4I5/)
 1003 FORMAT(10A1)
 1004 FORMAT(60A1,2I10/)
C**** 1005 FORMAT(10A1,8L4/)
 1005 FORMAT(10A1,3L4,5E14.7/)
C** 1006 FORMAT(5L4/)
 1006 FORMAT(E14.7,2L4,2E14.7/)
 1007 FORMAT(10A1,E14.7/)
 1021 FORMAT(10A1/100(9E14.7/))
 2001 FORMAT(F10.1,1X,F10.1,1X,I3,F10.1,1X,F10.1,1X,2F6.0,5I4,3F7.0)
 2100 FORMAT(10A1,60A1,/,I2,1X,I2,1X,I6,F6.0,I6,F6.0)
       END
