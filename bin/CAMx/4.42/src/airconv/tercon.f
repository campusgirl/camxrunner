       SUBROUTINE TERCON (IOUT,IDONE)
C
C
C      PROGRAM READS THE ASCII TERRAIN FILE THAT WILL BE INPUT INTO
C      THE SAI AIRSHED MODEL FOR LA AND REFORMATS THIS FILE SO
C      IT CAN BE USED AS MODEL INPUT
C
      INCLUDE 'nampar.cmd'
      REAL*8     IROUGH(MXX,MXY),IVEG(MXX,MXY)
      REAL*4     ROUGH(MXX,MXY),VEG(MXX,MXY)
Ctcm  DIMENSION TNAME(10,20)
      character*4 TNAME(10,20)
C
C
C   READ AND WRITE OUT FILE HEADER INFORMATION
C
      IDONE=1
      READ(7,2100) IFILE,NOTE,NSEG,NSPECS, IDATE, BEGTIM, JDATE, ENDTIM
      WRITE(9)IFILE,NOTE,NSEG,NSPECS,IDATE,BEGTIM,JDATE,ENDTIM
      WRITE (IOUT,1005) IDATE, BEGTIM, JDATE, ENDTIM
C  REGION DESCRIPTOR HEADER
      READ(7,2001) ORGX, ORGY, IZONE, UTMX, UTMY, DELTAX, DELTAY,
     $          NX, NY, NZ, NZLOWR, NZUPPR, HTSUR, HTLOW, HTUPP
      WRITE (9)      ORGX,ORGY,IZONE,UTMX,UTMY,DELTAX,DELTAY,NX,NY,
     $ NZ,NZLOWR,NZUPPR,HTSUR,HTLOW,HTUPP
C  SEGMENT ORIGIN AND SEGMENT SIZE
      READ(7,1002) IX, IY, NXCLL, NYCLL
      WRITE (9)      IX,IY,NXCLL,NYCLL
      IBHR=BEGTIM +1
      IEHR=ENDTIM
  10  CONTINUE
C*
C
C  TIME INVARIANT DATA
C
      IF (NSEG.EQ.0) GO TO 31
C
C  TIME INVARIANT DATA - SURFACE ROUGHNESS AND VEGFACTOR
C
      DO 30 I=1,NSEG
         READ (7,1021) ISEGM,(TNAME(II,1),II=1,10)
         READ (7,1022) ((IROUGH(II,JJ),II=1,NX),JJ=1,NY)
         DO 20 II=1,NX
            DO 20 JJ=1,NY
   20          ROUGH(II,JJ)=IROUGH(II,JJ)
         WRITE(9)      ISEGM,(TNAME(II,1),II=1,10),
     &      ((ROUGH(II,JJ),II=1,NX),JJ=1,NY)
         READ (7,1021) ISEGM,(TNAME(II,2),II=1,10)
         READ (7,1022) ((IVEG(II,JJ),II=1,NX),JJ=1,NY)
         DO 25 II=1,NX
            DO 25 JJ=1,NY
   25          VEG(II,JJ)=IVEG(II,JJ)
         WRITE(9)      ISEGM,(TNAME(II,2),II=1,10),
     &      ((VEG(II,JJ),II=1,NX),JJ=1,NY)
  30  CONTINUE
  31  CONTINUE
  999 RETURN
 1002 FORMAT(4I5)
 1003 FORMAT(10A1)
 1005 FORMAT(5X,2(I10,F10.2))
 1006 FORMAT(I4,10A1)
 1021 FORMAT(I10,10A1)
 1022 FORMAT(9D14.9)
 2001 FORMAT(F10.4,1X,F10.4,1X,I3,F10.4,1X,F10.4,1X,2F6.4,5I4,3F7.0)
 2100 FORMAT(10A1,60A1,/,I2,1X,I2,1X,I6,F6.0,I6,F6.0)
       END
